package propervariablesname;

public class propervariablename {
    public static  void  main(String[] args){
        //case sensitive
        //can be letters,digit,dollar signs or underscores
        //Must begin with a letter,dollar sign or underscore
        //cannot use Java keywords as variable name
        //camel case
        String myName="Mrinal";
        System.out.println(myName);
    }
}
